package com.andrgree.lifestat.rest.model;

public class Coord {

    private float lon;
    private float lat;

    /**
     * 
     * @return
     *     The lon
     */
    public float getLon() {
        return lon;
    }

    /**
     * 
     * @param lon
     *     The lon
     */
    public void setLon(float lon) {
        this.lon = lon;
    }

    /**
     * 
     * @return
     *     The lat
     */
    public float getLat() {
        return lat;
    }

    /**
     * 
     * @param lat
     *     The lat
     */
    public void setLat(float lat) {
        this.lat = lat;
    }

}
